export interface User {
  id?: string | number;
  username: string;
  role: string;
  status: 'ACTIVE' | 'INACTIVE';
  email?: string;
  locked?: boolean;
  moduleVisibility?: string[];
  permissions?: Permission[];
  temporaryPassword?: string;
  firstLogin?: boolean;
}

export interface Customer {
  id?: string | number;
  name: string;
  email: string;
  phone: string;
  joinDate?: string;
  status: 'ACTIVE' | 'INACTIVE';
}

