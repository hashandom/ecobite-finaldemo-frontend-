export interface Product {
  id: string; // Auto-generated by backend, e.g. PRODUCT001
  name: string;
  category: string;
  reorderLevel: number;
  unitPrice: number;
  stock?: number; // Usually returned in responses or update
}

export interface Batch {
  id?: string;
  productId: string;
  productName?: string;
  batchNumber: string;
  quantity: number;
  remainingQuantity?: number;
  manufactureDate: string;
  expiryDate: string;
  supplierId: number;
  locationId: number;
  purchasePrice: number;
  receivedDate?: string; // Might be mapped from backend or not needed
  status?: 'FRESH' | 'ACTIVE' | 'EXPIRING_SOON' | 'EXPIRED' | 'SPOILED' | 'RECALLED'; // Computed frontend side or returned by backend
  locationCode?: string;
  supplierName?: string;
}

export interface Supplier {
  id?: number;
  name: string;
  contactEmail: string;
  phone: string;
  rating?: number;
}

export interface Location {
  id?: number;
  warehouse: 'FREEZER' | 'DRY' | 'COLD' | string;
  section: string;
  shelf: string;
  capacity: number;
  currentOccupancy?: number;
  locationCode?: string;
}

export interface Notification {
  id: number | string;
  message: string;
  type: string;
  status: string;
  createdAt: string;
  isRead: boolean;
  targetRole: string;
  read?: boolean;
}

export interface Reorder {
  id: string;
  productId: string;
  quantity: number;
  status: string;
  createdAt: string;
}

export interface LowStockSupplier {
  productId: string;
  productName: string;
  currentStock: number;
  reorderLevel: number;
  supplierId: number | null;
  supplierName: string | null;
  supplierEmail: string | null;
  supplierPhone: string | null;
}

export interface InventoryAdjustment {
  id: string;
  productId: string;
  batchId?: string;
  type: 'IN' | 'OUT' | 'ADJUSTMENT';
  quantity: number;
  reason: string;
  date: string;
  user: string;
}

export interface DashboardStats {
  totalProducts: number;
  totalBatches: number;
  lowStockItems: number;
  activeAlerts: number;
  totalSuppliers: number;
  pendingReorders: number;
  warehouseCount: number;
  expiringSoon: number;
  recentActivity: {
    id: string;
    description: string;
    timestamp: string;
    type: 'ALERT' | 'INVENTORY' | 'SYSTEM';
  }[];
}

export interface Alert {
  id: string;
  productName: string;
  currentStock: number;
  severity: 'INFO' | 'WARNING' | 'CRITICAL';
  createdAt: string;
  resolved: boolean;
}

